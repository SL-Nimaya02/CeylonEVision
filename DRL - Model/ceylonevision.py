import numpy as np
import random
import gymnasium as gym
from gymnasium import spaces

class ColomboEVEnvironment(gym.Env):
    def __init__(self, stations_df, vehicles_df, behavior_df, risk_dict, traffic_matrix, traffic_delays,
                 random_start_battery=True, traffic_noise=2.0, verbose=False):
        super(ColomboEVEnvironment, self).__init__()

        self.stations = stations_df.to_dict('records')
        self.vehicles = vehicles_df.to_dict('records')
        self.traffic_matrix = np.array(traffic_matrix, dtype=np.float32)
        self.traffic_noise = traffic_noise
        self.random_start_battery = random_start_battery
        self.verbose = verbose

        all_station_ids = [s['Station_ID'] for s in self.stations]
        self.id_to_idx = {sid: i for i, sid in enumerate(all_station_ids)}

        self.action_space = spaces.Discrete(len(self.stations))
        self.observation_space = spaces.Box(low=-1e5, high=1e5, shape=(6,), dtype=np.float32)

        print(f"✅ Environment Initialized: Now with ECONOMIC COST PENALTIES 💰")

    def _get_obs(self):
        return np.array([
            float(self.lat_idx), float(self.lon_idx), float(self.current_battery_pct),
            float(self.current_vehicle_idx), float(self.last_dist), float(self.last_cost)
        ], dtype=np.float32)

    def reset(self, seed=None, options=None):
        super().reset(seed=seed)
        self.lat_idx = random.randint(0, len(self.traffic_matrix) - 1)
        self.lon_idx = random.randint(0, len(self.traffic_matrix[0]) - 1)
        self.current_vehicle_idx = random.randint(0, len(self.vehicles) - 1)
        self.current_vehicle = self.vehicles[self.current_vehicle_idx]
        self.current_battery_pct = np.random.uniform(2.0, 95.0) if self.random_start_battery else 25.0
        self.last_dist = 0.0
        self.last_cost = 0.0
        return self._get_obs(), {}

    def step(self, action):
        act_idx = int(action[0]) if isinstance(action, (np.ndarray, list)) else int(action)
        chosen = self.stations[act_idx]

        # 1. PHYSICAL CALCULATIONS
        base_dist = self.traffic_matrix[int(self.lat_idx)][act_idx]
        dist_km = max(0.1, base_dist + np.random.normal(0, self.traffic_noise))
        energy_needed = dist_km * self.current_vehicle['Drain_per_km_kWh']
        battery_kwh = (self.current_battery_pct / 100.0) * self.current_vehicle['Actual_Capacity_kWh']

        out_of_range = energy_needed > battery_kwh
        is_emergency = self.current_battery_pct < 15.0

        # 2. PARSE LKR COST FROM STATION
        try:
            price_str = str(chosen.get('Price_Details', '100'))
            # Get the first actual number from the text
            self.last_cost = float(''.join(filter(str.isdigit, price_str.split()[0])))
        except:
            self.last_cost = 100.0 # Default fallback price

        self.last_dist = dist_km

        # 3. 🚨 NEW MULTI-OBJECTIVE REWARD LOGIC (WITH COST) 🚨
        reward = 0
        if out_of_range:
            reward = -50000.0
            status = "💀 DEAD"
        else:
            dist_penalty = (dist_km ** 2) * 1.5

            # Multiply cost by a weight so it actually hurts the AI's score
            # E.g., if price is 150 LKR, penalty is -300 points
            cost_penalty = self.last_cost * 2.0

            if is_emergency:
                # MODE A: SURVIVAL (Ignore money, just live!)
                reward = 20000.0 - dist_penalty
                status = "🚨 EMERGENCY"
            else:
                # MODE B: STRATEGIC (Balance Speed, Proximity AND Price)
                reward = 5000.0 - dist_penalty + (chosen.get('Power_KW', 0) * 15.0) - cost_penalty
                status = "🟢 STRATEGIC"

        if self.verbose:
            print(f"Step: {status} | Dist: {dist_km:.2f}km | Cost: Rs.{self.last_cost} | Reward: {reward:.1f}")

        return self._get_obs(), float(reward), True, False, {"reason": "Success" if not out_of_range else "Dead"}

print("🧠 SUCCESS: Cost-Aware Environment Reconstructed. Multi-Objective AI now balances Distance, Power, and LKR Cost!")

