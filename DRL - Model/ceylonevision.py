def set_reproducible_seed(seed=42):
    random.seed(seed)
    np.random.seed(seed)

def evaluate_routing_success(env_args, num_episodes=500):
    set_reproducible_seed(42)
    print("🔄 Reconstructing Robust Environment for Full Research Evaluation...")

    def make_env():
        # Ensure it uses the ColomboEVEnvironment from Cell 6
        return ColomboEVEnvironment(**env_args)

    eval_env = DummyVecEnv([make_env])

    # 2. LOAD NORMALIZER (Crucial for correct AI interpretation)
    try:
        eval_env = VecNormalize.load(NORM_FILE, eval_env)
        eval_env.training = False
        eval_env.norm_reward = False
        print("✅ Normalization Statistics Loaded.")
    except Exception as e:
        print(f"⚠️ Warning: Could not load normalizer: {e}")

    # 3. LOAD MODEL WITH CUSTOM OBJECTS (THE FIX)
    # This overrides the strict space check and forces the model to accept
    # the slightly updated observation bounds of your current environment.
    custom_objects = {
        "observation_space": eval_env.observation_space,
        "action_space": eval_env.action_space
    }

    try:
        model = PPO.load(MODEL_FILE, env=eval_env, custom_objects=custom_objects)
        print(f"✅ AI Brain (PPO) Loaded. Testing N={num_episodes} stochastic scenarios...\n")
    except Exception as e:
        print(f"❌ Error loading model: {e}")
        return

    # Metrics dictionary
    stats = {"Success": 0, "Dead": 0}
    total_rewards = []
    emergency_success = 0
    emergency_total = 0

    for i in range(num_episodes):
        obs = eval_env.reset()

        # Peek at battery from the raw environment to track emergency sub-stats
        current_battery = eval_env.envs[0].current_battery_pct

        action, _ = model.predict(obs, deterministic=True)
        obs, reward, dones, infos = eval_env.step(action)

        reason = infos[0].get("reason", "Success")
        stats[reason] = stats.get(reason, 0) + 1
        total_rewards.append(reward[0])

        # Track how it performs specifically in low battery (<15%)
        if current_battery < 15.0:
            emergency_total += 1
            if reason == "Success":
                emergency_success += 1

    # 4. CALCULATE FINAL METRICS
    success_rate = (stats["Success"] / num_episodes) * 100
    em_success_rate = (emergency_success / emergency_total * 100) if emergency_total > 0 else 0

    print("\n" + "="*50)
    print(f"📊 IEEE RESEARCH METRICS: PPO-EV NAVIGATOR")
    print("="*50)
    print(f"✅ Overall Success Rate:   {success_rate:.2f}%")
    print(f"🚨 Emergency Success:      {em_success_rate:.2f}% (N={emergency_total})")
    print(f"💀 System Failures (Dead): {stats.get('Dead', 0)}")
    print(f"📈 Avg Normalized Reward:  {np.mean(total_rewards):.4f}")
    print("="*50)

    # 5. VISUALIZATION FOR DISSERTATION
    labels = ['Operational Success', 'Range Exhaustion (Dead)']
    sizes = [stats["Success"], stats.get("Dead", 0)]
    colors = ['#2ecc71', '#e74c3c']
    explode = (0.1, 0) # explode the success slice

    plt.figure(figsize=(12, 6))

    # Pie Chart
    plt.subplot(1, 2, 1)
    if sum(sizes) > 0:
        plt.pie(sizes, labels=labels, colors=colors, autopct='%1.1f%%',
                explode=explode, shadow=True, startangle=140)
    plt.title('Total Mission Success Rate')

    # Reward Distribution
    plt.subplot(1, 2, 2)
    if total_rewards:
        plt.hist(total_rewards, bins=20, color='#3498db', edgecolor='black')
    plt.title('Reward Distribution (Policy Confidence)')
    plt.xlabel('Normalized Reward')
    plt.ylabel('Frequency')

    plt.tight_layout()
    plt.show()

