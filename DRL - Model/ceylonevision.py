def fetch_validated_ev_data():
    print("🔄 Querying OpenChargeMap API... This may take a moment.")

    # API Configuration & Authentication
    url = "https://api.openchargemap.io/v3/poi"
    params = {
        "output": "json",
        "countrycode": "LK",     # Target: Sri Lanka ISO Code
        "maxresults": 5000,      # High-limit fetch for exhaustive research
        "verbose": True,
        "key": "27c3f7f9-6746-4ee7-9756-04238c9de7a6"
    }

    try:
        response = requests.get(url, params=params, timeout=20)
        response.raise_for_status()
        data = response.json()

        if not data:
            print("⚠️ API returned no data for Sri Lanka.")
            return None

    except Exception as e:
        print(f"❌ API Error: {e}")
        return None

    processed_stations = []

    for item in data:
        # 1. Attribute Extraction
        addr = item.get('AddressInfo', {})
        operator = item.get('OperatorInfo', {}).get('Title', 'Unknown Operator')
        phone = addr.get('ContactTelephone1', 'N/A')
        usage = item.get('UsageType', {}).get('Title', 'Public')
        status = item.get('StatusType', {}).get('Title', 'Operational')

        # 2. Heuristic Cost Imputation (Research Baseline)
        raw_cost = item.get('UsageCost', 'N/A')
        actual_cost = "75.00 LKR/kWh" if (not raw_cost or raw_cost == 'N/A') else raw_cost

        # 3. Connection (Port) Logic: Handling Denormalization
        connections = item.get('Connections', [])

        # Logic Case A: Station metadata exists but port details are missing
        if not connections:
            processed_stations.append({
                "Station_ID": str(item.get('ID')),
                "Name": addr.get('Title', 'Unnamed Station'),
                "Network": operator,
                "Phone": phone,
                "Town": addr.get('Town', 'N/A'),
                "Latitude": addr.get('Latitude'),
                "Longitude": addr.get('Longitude'),
                "Usage_Type": usage,
                "Price_Details": actual_cost,
                "Equipment_Spec": "Standard Plug",
                "Power_KW": 7.0,
                "Port_Count": 1,
                "Operational_Status": status
            })
        else:
            # Logic Case B: Relational Mapping (Create 1 row per unique Hardware Port)
            for i, conn in enumerate(connections):
                try:
                    power_val = float(conn.get('PowerKW') or 7.0)
                except:
                    power_val = 7.0

                processed_stations.append({
                    "Station_ID": f"{item.get('ID')}-{i}",  # Unique Hardware Identifier
                    "Name": addr.get('Title', 'Unnamed Station'),
                    "Network": operator,
                    "Phone": phone,
                    "Town": addr.get('Town', 'N/A'),
                    "Latitude": addr.get('Latitude'),
                    "Longitude": addr.get('Longitude'),
                    "Usage_Type": usage,
                    "Price_Details": actual_cost,
                    "Equipment_Spec": conn.get('ConnectionType', {}).get('Title', "Standard Plug"),
                    "Power_KW": power_val,
                    "Port_Count": conn.get('Quantity', 1),
                    "Operational_Status": status
                })

    # 4. Data Persistence & Export
    df = pd.DataFrame(processed_stations)
    df.to_csv(SAVE_PATH, index=False)
    return df

