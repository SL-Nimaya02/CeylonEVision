def build_colombo_backbone():
    """
    Synthesizes a routable traffic network (SUMO format) from OpenStreetMap data.
    Saves all artifacts (.net.xml, .rou.xml) to Google Drive.
    """
    print(f"🏙️ Synthesizing Backbone. Files will be saved to: {PROJECT_PATH}")

    # 1. TOOLCHAIN INITIALIZATION
    subprocess.run("sudo apt-get install -y osmium-tool", shell=True, check=True, stdout=subprocess.DEVNULL)

    # 2. FILESYSTEM MAPPING
    raw_osm = os.path.join(PROJECT_PATH, "sri_lanka_raw.osm.pbf")
    colombo_clipped = os.path.join(PROJECT_PATH, "colombo_clipped.osm.pbf")
    filtered_osm = os.path.join(PROJECT_PATH, "colombo_main.osm")
    net_file = os.path.join(PROJECT_PATH, "colombo_urban.net.xml")
    route_file = os.path.join(PROJECT_PATH, "colombo_urban.rou.xml")
    trips_file = os.path.join(PROJECT_PATH, "trips.trips.xml")

    # 3. SOURCE ACQUISITION
    if not os.path.exists(raw_osm):
        print("   -> Downloading Sri Lanka Map Source to Drive...")
        subprocess.run(f"wget -q https://download.geofabrik.de/asia/sri-lanka-latest.osm.pbf -O {raw_osm}", shell=True, check=True)

    # 4. SPATIAL CLIPPING
    print("   ✂️  Clipping Map to Colombo District...")
    bbox = "79.82,6.70,80.13,7.10"
    cmd_extract = f"osmium extract -b {bbox} {raw_osm} -o {colombo_clipped} --overwrite"
    try:
        subprocess.run(cmd_extract, shell=True, check=True, stdout=subprocess.DEVNULL)
    except subprocess.CalledProcessError:
        print("   ❌ Error: Failed to extract Colombo region.")
        return None, None

    # 5. SEMANTIC FILTERING
    print("   🧹 Filtering for Main Arteries...")
    tags_to_keep = "w/highway=motorway,motorway_link,trunk,trunk_link,primary,primary_link,secondary,secondary_link"
    cmd_filter = f"osmium tags-filter {colombo_clipped} {tags_to_keep} -o {filtered_osm} --overwrite"
    os.system(cmd_filter)

    # 6. NETWORK COMPILATION
    print("   🏗️ Compiling Colombo Traffic Network...")
    netconvert_cmd = [
        "netconvert",
        "--osm-files", filtered_osm,
        "--output-file", net_file,
        "--geometry.remove", "true",
        "--ramps.guess", "true",
        "--tls.guess", "true",
        "--junctions.join", "true",
        "--no-internal-links", "true",
        "--ignore-errors", "true",
        "--roundabouts.guess", "false",
        "--remove-edges.isolated", "true",
        "--verbose"
    ]

    try:
        subprocess.run(netconvert_cmd, check=True, stdout=subprocess.DEVNULL, stderr=subprocess.PIPE)
    except subprocess.CalledProcessError as e:
        if not os.path.exists(net_file):
             print(f"      ❌ Map Build Failed. Log:\n{e.stderr.decode()}")
             return None, None

    # 7. TRAFFIC DEMAND GENERATION
    print("   🚦 Generating Urban Traffic...")
    sumo_home = os.environ.get('SUMO_HOME', '/usr/share/sumo')
    random_trips = os.path.join(sumo_home, "tools/randomTrips.py")

    cmd = [
        "python3", random_trips,
        "-n", net_file,
        "-o", trips_file,
        "--route-file", route_file,
        "-e", "800",
        "--min-distance", "1500",
        "--validate"
    ]

    try:
        subprocess.run(cmd, check=True, stdout=subprocess.DEVNULL)
    except:
        print("      ⚠️ Traffic warning (ignorable).")

    # 8. ELECTRIC VEHICLE (EV) PHYSICS INJECTION
    ev_physics = """
    <vType id="ev_city" accel="2.5" decel="4.5" length="4.0" maxSpeed="40">
        <param key="device.battery.probability" value="1"/>
        <param key="device.battery.capacity" value="40000"/>
        <param key="device.battery.chargeLevel" value="20000"/>
    </vType>
    """

    if os.path.exists(route_file):
        with open(route_file, 'r') as f: content = f.read()
        if '<routes>' in content:
            new_content = content.replace('<routes>', f'<routes>\n{ev_physics}')
            new_content = new_content.replace('type="DEFAULT_VEHTYPE"', 'type="ev_city"')
            with open(route_file, 'w') as f: f.write(new_content)

    print(f"✅ Colombo Simulation Ready and Saved to Drive: {net_file}")
    return net_file, route_file

